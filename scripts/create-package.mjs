import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

const ROOT_DIR = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const PACKAGES_DIR = resolve(ROOT_DIR, 'packages');
const ROOT_PACKAGE_JSON = resolve(ROOT_DIR, 'package.json');

const DEFAULT_SCOPE = '@agrimetrics';

const toKebabCase = (value) =>
  value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

const parseArguments = () => {
  const args = process.argv.slice(2);
  const rawName = args.find((arg) => !arg.startsWith('--'));

  if (!rawName) {
    throw new Error('Usage: pnpm run new:package -- <package-name> [--scope=@agrimetrics]');
  }

  const scopeArg = args.find((arg) => arg.startsWith('--scope='));
  const scope = scopeArg ? scopeArg.split('=')[1] : DEFAULT_SCOPE;

  if (!scope.startsWith('@')) {
    throw new Error('Scope must start with @, for example --scope=@agrimetrics');
  }

  const directoryName = toKebabCase(rawName);

  if (!directoryName) {
    throw new Error('Package name cannot be empty after normalization');
  }

  return {
    directoryName,
    packageName: `${scope}/${directoryName}`,
  };
};

