const tsconfigTemplate = `{
  "extends": "../../packages/typescript-config/library.json",
  "compilerOptions": {
    "types": ["node", "vitest/globals"],
    "paths": {
      "@/*": ["./src/*"]
    }
  },
  "include": ["src"],
  "exclude": [
    "node_modules",
    "dist",
    "eslint.config.ts",
    "prettier.config.ts",
    "release.config.ts",
    "vite.config.ts"
  ]
}
`;

const eslintTemplate = `import { createLibraryEslintConfig } from '@dsp-toolkit/shared-config/eslint';

export default createLibraryEslintConfig({
  globals: {
    process: 'readonly',
  },
});
`;

const prettierTemplate = `import prettierConfig from '@dsp-toolkit/shared-config/prettier';

export default prettierConfig;
`;

const releaseTemplate = ({ directoryName }) => `import releaseConfig from '@dsp-toolkit/shared-config/release';

export default {
  ...releaseConfig,
  tagFormat: '${directoryName}-v\${version}',
};
`;

const viteTemplate = `import { createSimpleLibraryViteConfig } from '@dsp-toolkit/shared-config/vite';

export default createSimpleLibraryViteConfig(import.meta.url);
`;

const readmeTemplate = ({ packageName }) => `# ${packageName}

Package generated by DSP Toolkit scaffold.

## Scripts

- pnpm build
- pnpm lint
- pnpm test:ci
`;

const indexTemplate = `// Replace with real exports during migration.
export {};
`;

const smokeTestTemplate = ({ packageName }) => `import * as pkg from './index';

// Smoke tests: verify the package resolves and exports cleanly.
// Todos are stubs to implement after source migration.

describe('${packageName}', () => {
  it('resolves the package entrypoint', () => {
    expect(pkg).toBeDefined();
  });

  it.todo('exports a primary class or function');
});
`;

const updateRootScripts = ({ directoryName, packageName }) => {
  const rootPackage = JSON.parse(readFileSync(ROOT_PACKAGE_JSON, 'utf8'));
  const scripts = rootPackage.scripts ?? {};

  const qualityScriptName = `quality:ci:${directoryName}`;
  const releaseScriptName = `release:${directoryName}`;

  scripts[qualityScriptName] = `pnpm run quality:ci -- --filter=${packageName}`;
  scripts[releaseScriptName] = `pnpm --filter ${packageName} exec semantic-release`;

  rootPackage.scripts = scripts;
  writeFileSync(ROOT_PACKAGE_JSON, `${JSON.stringify(rootPackage, null, 2)}\n`);
};

const writePackageFiles = ({ directoryName, packageName }) => {
  const packageDir = resolve(PACKAGES_DIR, directoryName);

  if (existsSync(packageDir)) {
    throw new Error(`Package directory already exists: ${packageDir}`);
  }

  mkdirSync(resolve(packageDir, 'src'), { recursive: true });

  writeFileSync(resolve(packageDir, 'package.json'), packageJsonTemplate({ packageName, directoryName }));
  writeFileSync(resolve(packageDir, 'tsconfig.json'), tsconfigTemplate);
  writeFileSync(resolve(packageDir, 'eslint.config.ts'), eslintTemplate);
  writeFileSync(resolve(packageDir, 'prettier.config.ts'), prettierTemplate);
  writeFileSync(resolve(packageDir, 'release.config.ts'), releaseTemplate({ directoryName }));
  writeFileSync(resolve(packageDir, 'vite.config.ts'), viteTemplate);
  writeFileSync(resolve(packageDir, 'README.md'), readmeTemplate({ packageName }));
  writeFileSync(resolve(packageDir, 'src/index.ts'), indexTemplate);
  writeFileSync(resolve(packageDir, 'src/index.test.ts'), smokeTestTemplate({ packageName }));
};

try {
  const config = parseArguments();
  writePackageFiles(config);
  updateRootScripts(config);

  console.log(`Created package ${config.packageName} in packages/${config.directoryName}`);
  console.log('Next steps:');
  console.log('1) pnpm install');
  console.log(`2) pnpm run quality:ci:${config.directoryName}`);
  console.log('3) Add package to .github/workflows/release.yml options and ALL_PACKAGES when it should be releasable');
} catch (error) {
  const message = error instanceof Error ? error.message : String(error);
  console.error(`new:package failed: ${message}`);
  process.exit(1);
}
